export function getProjectBySlug(slug) {
  return PROJECTS.find((p) => p.slug === slug);
}

export function getAdjacentProjects(slug) {
  const idx = PROJECTS.findIndex((p) => p.slug === slug);
  if (idx === -1) return { prev: null, next: null };
  const prev = idx > 0 ? PROJECTS[idx - 1] : PROJECTS[PROJECTS.length - 1];
  const next = idx < PROJECTS.length - 1 ? PROJECTS[idx + 1] : PROJECTS[0];
  return { prev, next };
}

function getYouTubeId(url) {
  if (!url || typeof url !== "string") return null;
  try {
    const u = new URL(url);
    const host = u.hostname.replace(/^www\./, "");
    if (
      host !== "youtu.be" &&
      host !== "youtube.com" &&
      host !== "youtube-nocookie.com" &&
      host !== "m.youtube.com"
    ) {
      return null;
    }
    if (host === "youtu.be") {
      return u.pathname.slice(1).split("/")[0] || null;
    }
    const vParam = u.searchParams.get("v");
    if (vParam && /^[A-Za-z0-9_-]{11}$/.test(vParam)) return vParam;
    const parts = u.pathname.split("/").filter(Boolean);
    const last = parts[parts.length - 1];
    if (last && /^[A-Za-z0-9_-]{11}$/.test(last)) return last;
    return null;
  } catch {
    return null;
  }
}

function getVimeoInfo(url) {
  if (!url || typeof url !== "string") return null;
  try {
    const u = new URL(url);
    const host = u.hostname.replace(/^www\./, "");
    if (host !== "vimeo.com" && host !== "player.vimeo.com") {
      return null;
    }
    const parts = u.pathname.split("/").filter(Boolean);
    const idIdx = parts.findIndex((p) => /^\d+$/.test(p));
    if (idIdx === -1) return null;
    const id = parts[idIdx];
    let hash = u.searchParams.get("h");
    if (!hash && idIdx + 1 < parts.length) {
      const next = parts[idIdx + 1];
      if (/^[A-Za-z0-9]+$/.test(next)) hash = next;
    }
    return { id, hash: hash || null };
  } catch {
    return null;
  }
}

export function parseVideoUrl(url) {
  const ytId = getYouTubeId(url);
  if (ytId) return { provider: "youtube", id: ytId };
  const vimeo = getVimeoInfo(url);
  if (vimeo) return { provider: "vimeo", id: vimeo.id, hash: vimeo.hash };
  return null;
}
